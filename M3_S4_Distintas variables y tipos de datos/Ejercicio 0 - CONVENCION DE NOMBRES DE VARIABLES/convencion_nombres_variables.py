""" Existen varios sistemas que se utilizan, dependiendo de que queramos nombrar:

• lowerCamelCase: este sistema consiste en poner todas las palabras del nombre sin espacios, 
    escritas en minúscula, excepto la primera letra de cada palabra a partir de la segunda palabra.
    
• UpperCamelCase: este sistema es idéntico al anterior, excepto porque la primera letra de la primera 
    palabra también va en mayúscula.
    
• snake_case: en este caso todas las palabras se escriben en minúsculas, y los espacios se sustituyen 
    por guiones de piso.
    
• SCREAMING_SNAKE_CASE: este caso es igual al anterior, pero con todas las letras en mayúsculas.

Cuando deseamos ponerle nombre a una variable en Python, usualmente el sistema a usar es el snake_case.

Si deseamos utilizar simplemente una letra para nombrar a una variable debemos evitar usar la O mayúscula 
para evitar se confunda con el cero. Evitar usar la l minúscula (ele), pues podría confundirse con un uno, 
lo que también podría ocurrir con la I mayúscula (i). """