# Sort es un metodo que modifica la actual lista de datos

numero = [1, 2, 3, 4, 5, 6, 7, 8]
numero.sort(reverse=True)
print(numero)

# Sorted es una funcion que debes crear una nueva lista para que se guarden los datos.

numeros = [1, 2, 3, 4, 5, 6, 7, 8]
numerosOrdenados = sorted(numeros, reverse=True)
print(numerosOrdenados)
